Num1 = int(input("Enter Number1: "))
Num2 = int(input("Enter Number2: "))

print(f"AND ({Num1} & {Num2}):", Num1 & Num2)
print(f"OR ({Num1} | {Num2}):", Num1 | Num2)
print(f"XOR ({Num1} ^ {Num2}):", Num1 ^ Num2)
print(f"SUM ({Num1} + {Num2}):", Num1 + Num2)
print(f"SUB ({Num1} - {Num2}):", Num1 - Num2)
print(f"MUL ({Num1} * {Num2}):", Num1 * Num2)
print(f"DIV ({Num1} / {Num2}):", Num1 / Num2)
print(f"LAR ({Num1} > {Num2}):", Num1 > Num2)
print(f"LOW ({Num1} < {Num2}):", Num1 < Num2)
print(f"EQ ({Num1} == {Num2}):", Num1 == Num2)



