#ifndef LEDANIMATION_LIB_STD_TYPES_H_
#define LEDANIMATION_LIB_STD_TYPES_H_

// Define data types for unsigned integers
typedef unsigned char       u8;
typedef unsigned short int  u16;
typedef unsigned long int   u32;

// Define data types for signed integers
typedef signed char         s8;
typedef signed short int    s16;
typedef signed long int     s32;

// Define data types for floating-point numbers
typedef float               f32;
typedef double              f64;

#endif /* LEDANIMATION_LIB_STD_TYPES_H_ */
